package org.liufeng.course.pojo;

public class Location {
	
	private double X;
	private double y;
	public Location(double X,double Y){
		this.X = X;
		this.y = Y;
	}
	
	public double getX() {
		return X;
	}
	public void setX(double x) {
		X = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	

}
