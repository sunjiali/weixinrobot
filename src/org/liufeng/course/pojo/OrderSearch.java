package org.liufeng.course.pojo;

public class OrderSearch {
	
	private int id;
	private String open_id;
	private String create_time;
	private String req_express ;
	private String req_order;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOpen_id() {
		return open_id;
	}
	public void setOpen_id(String open_id) {
		this.open_id = open_id;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getReq_express() {
		return req_express;
	}
	public void setReq_express(String req_express) {
		this.req_express = req_express;
	}
	public String getReq_order() {
		return req_order;
	}
	public void setReq_order(String req_order) {
		this.req_order = req_order;
	}
	

}
